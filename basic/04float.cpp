// increment and decrement operators
#include <iostream>
using namespace std;
int main()
{
    int a=5;
    cout<<a<<"\n";
    a=a+1;
    cout<<a<<"\n";  //this code is for increment
    a+=1;
    cout<<a<<"\n";
    a++;
    cout<<a<<"\n";
    a=a-1;
    cout<<a<<"\n";  //this code is for decrement
    a-=1;
    cout<<a<<"\n";
    a--;
    cout<<a<<"\n";

}